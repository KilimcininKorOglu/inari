package runner

import (
	"crypto/sha256"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

const manifestFilename = ".fixture-manifest.sha256"

// ManifestEntry represents a single file in a fixture manifest.
type ManifestEntry struct {
	Path string `json:"path"`
	Hash string `json:"hash"`
}

// GenerateManifest creates a SHA-256 manifest for all source files in a fixture directory.
// Returns the list of entries with their paths and hashes.
func GenerateManifest(fixtureDir string) ([]ManifestEntry, error) {
	entries, err := collectSourceFiles(fixtureDir)
	if err != nil {
		return nil, err
	}

	if len(entries) == 0 {
		return nil, fmt.Errorf("no source files found in %s. Is this a valid fixture directory?", fixtureDir)
	}

	// Write the manifest file
	manifestPath := filepath.Join(fixtureDir, manifestFilename)
	f, err := os.Create(manifestPath)
	if err != nil {
		return nil, fmt.Errorf("failed to create manifest at %s: %w", manifestPath, err)
	}
	defer f.Close()

	fmt.Fprintln(f, "# Generated by: benchmark manifest --generate")
	fmt.Fprintln(f, "# Do not edit manually")

	for _, entry := range entries {
		fmt.Fprintf(f, "%s  %s\n", entry.Hash, entry.Path)
	}

	fmt.Fprintf(os.Stderr, "Manifest generated: %d files hashed -> %s\n", len(entries), manifestPath)

	return entries, nil
}

// VerifyManifest checks all source files in a fixture match their manifest hashes.
// Returns (true, nil) if all hashes match, or (false, list of differences) if not.
func VerifyManifest(fixtureDir string, manifest []ManifestEntry) (bool, []string) {
	// If no manifest provided, load from file
	if manifest == nil {
		manifestPath := filepath.Join(fixtureDir, manifestFilename)
		loaded, err := loadManifestFromFile(manifestPath)
		if err != nil {
			return false, []string{err.Error()}
		}
		manifest = loaded
	}

	if len(manifest) == 0 {
		return false, []string{"manifest is empty or malformed"}
	}

	var failures []string

	for _, entry := range manifest {
		fullPath := filepath.Join(fixtureDir, entry.Path)
		if !fileExists(fullPath) {
			failures = append(failures, fmt.Sprintf("  MISSING: %s", entry.Path))
			continue
		}

		actualHash, err := hashFile(fullPath)
		if err != nil {
			failures = append(failures, fmt.Sprintf("  ERROR: %s: %v", entry.Path, err))
			continue
		}

		if actualHash != entry.Hash {
			failures = append(failures, fmt.Sprintf("  MODIFIED: %s", entry.Path))
		}
	}

	// Check for new files not in manifest
	expectedPaths := make(map[string]struct{})
	for _, entry := range manifest {
		expectedPaths[entry.Path] = struct{}{}
	}

	currentFiles, err := collectSourceFiles(fixtureDir)
	if err == nil {
		for _, entry := range currentFiles {
			if _, ok := expectedPaths[entry.Path]; !ok {
				failures = append(failures, fmt.Sprintf("  NEW: %s", entry.Path))
			}
		}
	}

	if len(failures) == 0 {
		fmt.Fprintf(os.Stderr, "Manifest OK: %d files verified in %s\n", len(manifest), fixtureDir)
		return true, nil
	}

	return false, failures
}

// loadManifestFromFile reads a manifest file and returns the entries.
func loadManifestFromFile(manifestPath string) ([]ManifestEntry, error) {
	if !fileExists(manifestPath) {
		return nil, fmt.Errorf("no manifest found at %s. Run 'benchmark manifest --generate' first", manifestPath)
	}

	data, err := os.ReadFile(manifestPath)
	if err != nil {
		return nil, fmt.Errorf("failed to read manifest at %s: %w", manifestPath, err)
	}

	var entries []ManifestEntry
	for _, line := range strings.Split(string(data), "\n") {
		line = strings.TrimSpace(line)
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}
		parts := strings.SplitN(line, "  ", 2)
		if len(parts) != 2 {
			continue
		}
		entries = append(entries, ManifestEntry{
			Hash: parts[0],
			Path: parts[1],
		})
	}

	if len(entries) == 0 {
		return nil, fmt.Errorf("manifest at %s is empty or malformed", manifestPath)
	}

	return entries, nil
}

// collectSourceFiles finds all source files and computes their SHA-256 hashes.
func collectSourceFiles(fixtureDir string) ([]ManifestEntry, error) {
	sourceExtensions := map[string]bool{
		".ts": true, ".tsx": true, ".js": true, ".jsx": true,
		".cs": true, ".json": true, ".csproj": true, ".sln": true,
		".py": true, ".pyi": true,
		".rs": true, ".toml": true,
	}

	var entries []ManifestEntry

	err := filepath.Walk(fixtureDir, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			fmt.Fprintf(os.Stderr, "  Warning: skipping entry during manifest scan: %v\n", err)
			return nil
		}

		if info.IsDir() {
			return nil
		}

		ext := filepath.Ext(path)
		if !sourceExtensions[ext] {
			return nil
		}

		relPath, err := filepath.Rel(fixtureDir, path)
		if err != nil {
			return fmt.Errorf("failed to compute relative path: %w", err)
		}

		// Normalize path separators
		relStr := strings.ReplaceAll(relPath, "\\", "/")

		// Skip excluded directories
		if strings.HasPrefix(relStr, "node_modules/") ||
			strings.HasPrefix(relStr, ".inari/") ||
			strings.HasPrefix(relStr, "bin/") ||
			strings.HasPrefix(relStr, "obj/") ||
			relStr == manifestFilename {
			return nil
		}

		hash, err := hashFile(path)
		if err != nil {
			return err
		}

		entries = append(entries, ManifestEntry{
			Path: relStr,
			Hash: hash,
		})

		return nil
	})
	if err != nil {
		return nil, err
	}

	sort.Slice(entries, func(i, j int) bool {
		return entries[i].Path < entries[j].Path
	})

	return entries, nil
}

// hashFile computes the SHA-256 hash of a file, returned as a hex string.
func hashFile(path string) (string, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return "", fmt.Errorf("failed to read file for hashing: %s: %w", path, err)
	}
	hash := sha256.Sum256(data)
	return fmt.Sprintf("%x", hash), nil
}
